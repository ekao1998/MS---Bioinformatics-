# check the frequency:
freq = []
for x in hold_list: #pass letter by letter
    count = 0
    for key, value in codon_map.items(): #a set-like object providing a view on D's items check how many possible keys match to the value
        if x  == value:
            count+=1
    freq.append(count) 

# print(freq)

def calculate(x):
    possible_seq = 1 #initial set to 1
    for i in x: # 34~40 lines will get every possible frequency for the letter, here we iterate each letter's freq to update total possible freq
        possible_seq *= i
    possible_seq*=3 #stop codon have 3, UAA ,UAG, UGA
    print(possible_seq%1000000) #modulo 1,000,000



print(calculate(freq))